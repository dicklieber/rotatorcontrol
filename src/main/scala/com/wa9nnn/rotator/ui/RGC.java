package com.wa9nnn.rotator.ui;

public class RGC {
    private String name;
    private String host;

    public RGC() {
    }

    public String getName() {
        return name;
    }

    public void setName(final String name) {
        this.name = name;
    }

    public String getHost() {
        return host;
    }

    public void setHost(final String host) {
        this.host = host;
    }
}